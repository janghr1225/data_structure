#include <stdio.h>
#include "ArrayList.h"

int main(void)
{
	/*** ArrayList의 생성 및 초기화 ***/
	List list;
	int data;
	ListInit(&list);
	int sum = 0;

	/*** 9개의 데이터 저장 ***/
	/*LInsert(&list, 11);  LInsert(&list, 11);
	LInsert(&list, 22);  LInsert(&list, 22);
	LInsert(&list, 33);*/

	for (int i = 0; i < 9; i++) {
		LInsert(&list,i + 1);		//데이터 저장
	}

	/*** 순차적으로 참조하여 그 합을 계산 ***/
	if (LFirst(&list, &data)) {		//첫 번째 데이터 조회
		
		sum = data;

		while (LNext(&list, &data))		//두 번째 이후 데이터 조회
			sum = sum + data;
	}

	/*** 저장된 데이터의 전체 출력 ***/
	printf("현재 데이터의 수: %d \n", LCount(&list));
	printf("현재 데이터들의 합 : %d\n\n", sum);

	if(LFirst(&list, &data))    // 첫 번째 데이터 조회
	{
		printf("%d ", data);
		
		while(LNext(&list, &data))    // 두 번째 이후의 데이터 조회
			printf("%d ", data);
	}
	printf("\n\n");

	/*** 숫자 2와 3의 배수를 탐색하여 모두 삭제 ***/
	if(LFirst(&list, &data)){
		if(data % 2 == 0 || data % 3 == 0)
			LRemove(&list);

		while(LNext(&list, &data))
		{
			if (data % 2 == 0 || data % 3 ==0)
				LRemove(&list);
		}
	}

	/*** 삭제 후 저장된 데이터 전체 출력 ***/
	printf("현재 데이터의 수: %d \n", LCount(&list));

	if(LFirst(&list, &data)){
		printf("%d ", data);
		
		while(LNext(&list, &data))
			printf("%d ", data);
	}
	printf("\n\n");
	return 0;
}